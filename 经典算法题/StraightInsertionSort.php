$arr = [1, 3, 34, 2, 32, 2, 78, -43, 53, -35, 0];

class Solution
{
    function StraightInsertionSort($arr){
        $length=count($arr);
        for($i=1;$i<$length;$i++){
            $get=$arr[$i];
            $j=$i-1;
            while ($j>=0 && $arr[$j]>$get){
                $arr[$j+1]=$arr[$j];
                $j--;
            }
            $arr[$j+1]=$get;
        }
        return $arr;
    }

}