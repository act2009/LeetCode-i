$arr = [1, 3, 34, 2, 32, 2, 78, -43, 53, -35, 0];
class Solution{

    function BubbleSort($arr){
        $length=count($arr);
        for($i=0;$i<$length;$i++){
            for($j=0;$j<$length-$i;$j++){
                if($arr[$j]>$arr[$j+1]){
                    $temp=$arr[$j];
                    $arr[$j]=$arr[$j+1];
                    $arr[$j+1]=$temp;
                }
            }
        }
        return $arr;
    }
}